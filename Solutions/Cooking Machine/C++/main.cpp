// Problem: Cooking Machine
// Link to the problem: https://www.codechef.com/problems/COOKMACH
#include <bits/stdc++.h>
#define ll long long int
#define ull unsigned long long int
using namespace std;

void solve()
{
    ll a, b;
    cin >> a >> b;
    ll ans = 0;
    while ((a & -a) != a)
    {
        a /= 2;
        ans++;
    }
    while (a < b)
    {
        a *= 2;
        ans++;
    }
    while (a > b)
    {
        a /= 2;
        ans++;
    }
    cout << ans << endl;
}

int main()
{
    // freopen("input.txt", "r", stdin);
    // freopen("output.txt", "w", stdout);
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);
    ll t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}