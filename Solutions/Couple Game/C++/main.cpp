#include <bits/stdc++.h>
#define ll long long int
#define ull unsigned long long int
using namespace std;

int main()
{
    ll t;
    cin >> t;
    while (t--)
    {
        ll g, b;
        cin >> g >> b;
        cout << b - g << endl;
    }
    return 0;
}