
// Problem: Chef and Brain Speed
// Link to the problem: https://www.codechef.com/problems/CBSPEED
import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int x = sc.nextInt();
        int y = sc.nextInt();
        if (y > x) {
            System.out.println("YES");
        } else {
            System.out.println("No");
        }
    }
}
