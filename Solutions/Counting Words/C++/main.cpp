// Problem: Counting Words
// Link to the problem: https://www.codechef.com/problems/CNTWRD
#include <bits/stdc++.h>
#define ll long long int
#define ull unsigned long long int
using namespace std;

int main()
{
    ll t;
    cin >> t;
    while (t--)
    {
        ll n, m;
        cin >> n >> m;
        cout << n * m << endl;
    }
    return 0;
}